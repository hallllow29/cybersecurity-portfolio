package Enums;

/**
 * Represents the type of a device, indicating whether the device is intended
 * to function as an input or output device.
 *
 * INPUT:
 *     Represents devices used to send data or control signals to a system.
 *     Examples might include keyboards, mice, and microphones.
 *
 * OUTPUT:
 *     Represents devices used to receive data or control signals from a system.
 *     Examples might include monitors, speakers, and printers.
 *
 * This enumeration allows for consistent categorization and identification of
 * devices within a system, supporting seamless management and interaction with
 * different device types.
 */
public enum DeviceType {
    INPUT, OUTPUT;
}
